package at.fhtw.mctg.model;

/**
 * Represents the possible outcomes of a battle.
 * This enumeration is used to define the result status for a participant in a battle.
 *
 * WIN - Indicates the participant won the battle.
 * LOSS - Indicates the participant lost the battle.
 * TIE - Indicates the battle ended in a tie between participants.
 */
public enum BattleStatus {
    WIN,
    LOSS,
    TIE
}
