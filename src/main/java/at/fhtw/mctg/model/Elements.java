package at.fhtw.mctg.model;

/**
 * Enumeration representing the elements associated with a card.
 * Elements define the elemental attributes or nature of a card,
 * and can influence the gameplay and interactions between cards.
 *
 * The supported element types are:
 *
 * - FIRE: Represents cards associated with the fire element.
 * - WATER: Represents cards associated with the water element.
 * - NORMAL: Represents cards with no elemental attribute or a neutral nature.
 */
public enum Elements {
    FIRE,
    WATER,
    NORMAL
}
